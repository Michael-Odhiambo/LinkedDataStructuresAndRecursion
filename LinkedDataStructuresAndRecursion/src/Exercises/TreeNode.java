package Exercises;

/**
 * This class represents a node in a Binary Search Tree.
 */
public class TreeNode {

    String word;  // The word in this node.
    TreeNode left;  // The left child of this node.
    TreeNode right;  // The right child of this node.

    // Constructor. Creates a TreeNode containing the specified string.
    // Note that the left and right child are null.
    TreeNode( String word ) {
        this.word = word;

    }

}
